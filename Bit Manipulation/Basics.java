

public class Basics {
    public static void main(String[] args) {
        int a=5;
        int b=6;
        // Binary '&'
        System.out.println("Binary '&': " + (a&b)); 

        // Binary '|'
        System.out.println("Binary '|': "+(a|b));

        // Binary '^'
        System.out.println("BInary '|': "+(a^b));

        // Binary one's Complement
        System.out.println(~a);

        // Binary left shift
        System.out.println("left shift: "+(5<<2));
        

        // Binary Right shift
        System.out.println("Right shift: "+(6>>1));


        
    }
}
