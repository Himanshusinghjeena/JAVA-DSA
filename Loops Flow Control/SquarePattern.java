public class SquarePattern {
    public static void main(String[] args) {

        // Using for Loop to Print a Square Pattern
        for (int lines=1;lines<=4;lines++)
        {
            System.out.println("* * * *");
        }

        // Using a While Loop to Print a Square Pattern
        int lines=0;
        while(lines<=4)
        {
            System.out.println("* * * *");
            lines++;
        }
    }
}
